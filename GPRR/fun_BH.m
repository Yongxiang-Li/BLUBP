function z=fun_BH(t)

r_w=0.05+(0.15-0.05)*t(:,1);
r=100+(50000-100)*t(:,2);
T_u=63070+(115600-63070)*t(:,3);
H_u=990+(1110-990)*t(:,4);
T_l=63.1+(116-63.1)*t(:,5);
H_l=700+(820-700)*t(:,6);
L=1120+(1680-1120)*t(:,7);
K_w=1500+(15000-1500)*t(:,8);

z=(2*pi*T_u.*(H_u-H_l))./(log(r./r_w).*(1+(2*L.*T_u)./(log(r./r_w).*r_w.^2.*K_w)+T_u./T_l));
z = z + 1*randn(size(z));